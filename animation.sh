    printf "${green}SUCCESS!!!${reset}\n"
    sleep 1

    while [ $i -lt $length ]; do
        # 各文字を元の色で出力
        printf "%s" "${text:$i:1}"
        sleep $delay
        i=$((i + 1))
    done
    echo ""
}

